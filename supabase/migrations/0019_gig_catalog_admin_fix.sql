-- В 0018 в политику чтения задач попало «or public.is_admin()» — по привычке, как на
-- остальных таблицах, где админу нужен полный доступ для модерации. Но задачи в панели
-- не модерируются: там файлы, заявки компаний, смены статуса и справки. Задач нет.
--
-- Единственным следствием было то, что админ видел в каталоге закрытые и заполненные
-- задачи, которых не видит никто другой, — и выглядело это как поломка фильтра.
--
-- Компания по-прежнему видит свои задачи всегда: без этого «Мои вакансии» опустели бы
-- ровно тогда, когда там появляется самое важное — завершение и выдача справок.

drop policy if exists "gigs_select_open" on public.gigs;
create policy "gigs_select_open" on public.gigs
  for select
  using (
    public.gig_is_open(id, slots, closed_at)
    or public.owns_company_app(company_app_id)
  );
