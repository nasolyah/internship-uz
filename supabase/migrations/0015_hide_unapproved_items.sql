-- В 0012 неодобренный файл вырезался из элемента, но сам элемент оставался: навык
-- «Python» показывался компании сразу, просто без приложенного сертификата. Логика была
-- в том, что название навыка скрывать незачем.
--
-- На практике это читается иначе: студент прикладывает документ именно чтобы подтвердить
-- навык, и до проверки заявка выглядит как уже подтверждённая. Поэтому теперь элемент
-- с неодобренным файлом не показывается целиком.
--
-- Элементы без файла ведут себя как раньше и показываются всегда: там нечего модерировать,
-- это просто самоописание. Проекты тоже не трогаем — у проекта есть собственная ценность
-- (название, описание, теги) помимо приложенных файлов, поэтому там по-прежнему
-- отфильтровываются только сами файлы.

create or replace function public.keep_approved_file(items jsonb, ok text[])
returns jsonb
language sql
immutable
as $$
  select coalesce(
    jsonb_agg(it) filter (
      where it -> 'file' is null                    -- нечего подтверждать — показываем
         or (it -> 'file' ->> 'path') = any(ok)     -- файл одобрен — показываем с файлом
    ),
    '[]'::jsonb
  )
  from jsonb_array_elements(coalesce(items, '[]'::jsonb)) as it;
$$;
